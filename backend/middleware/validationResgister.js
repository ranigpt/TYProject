const { body, validationResult } = require("express-validator");

const validateRegistration = [
  body("Name").isLength({ min: 3 , max:50 }).withMessage("Name must be at least 3 characters"),
  body("Email").isEmail().withMessage("Must be a valid email")
  .matches(/(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|"(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21\x23-\x5b\x5d-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])*")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\[(?:(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9]))\.){3}(?:(2(5[0-5]|[0-4][0-9])|1[0-9][0-9]|[1-9]?[0-9])|[a-z0-9-]*[a-z0-9]:(?:[\x01-\x08\x0b\x0c\x0e-\x1f\x21-\x5a\x53-\x7f]|\\[\x01-\x09\x0b\x0c\x0e-\x7f])+)\])/).withMessage('Email must follow the format: example@domain.com')
  ,
  body("Dob").isDate().withMessage("Date of birth must be valid"),
  body("Password")
    .isLength({ min: 6 , max:30}).withMessage("Password must be at least 6 characters long")
    .matches(/\d/).withMessage("Password must contain at least one number")
];

const handleValidationErrors = (req, res, next) => {
  const errors = validationResult(req);
  if (!errors.isEmpty()) {
    return res.status(400).json({ errors: errors.array() });
  }
  next();
};

module.exports = { validateRegistration, handleValidationErrors };
